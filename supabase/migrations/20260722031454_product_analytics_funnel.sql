create schema if not exists analytics;

revoke all on schema analytics from public, anon, authenticated;
grant usage on schema analytics to service_role;

create table public.product_events (
  id bigint generated by default as identity primary key,
  visitor_id uuid,
  user_id uuid references public.users(id) on update cascade on delete set null,
  experiment_id uuid references public.weekly_experiments(id) on update cascade on delete set null,
  event_name text not null,
  source text not null,
  plan text,
  step smallint,
  event_key text unique,
  created_at timestamptz not null default now(),
  constraint product_events_identity check (visitor_id is not null or user_id is not null),
  constraint product_events_name check (event_name in (
    'homepage_viewed', 'pricing_viewed', 'intake_started', 'intake_completed',
    'blueprint_viewed', 'blueprint_action_selected', 'checkout_started',
    'checkout_completed', 'activation_started', 'activation_completed',
    'practice_completed', 'check_in_completed', 'weekly_review_opened',
    'weekly_review_completed', 'subscription_cancelled'
  )),
  constraint product_events_source check (source in (
    'homepage', 'pricing', 'tally', 'results', 'upgrade', 'stripe',
    'onboarding', 'today', 'daily_log', 'weekly_review'
  )),
  constraint product_events_plan check (plan is null or plan in ('monthly', 'annual')),
  constraint product_events_step check (step is null or step between 0 and 6),
  constraint product_events_event_key_length check (event_key is null or char_length(event_key) between 8 and 180)
);

create index product_events_user_created_idx
  on public.product_events (user_id, created_at desc)
  where user_id is not null;

create index product_events_visitor_created_idx
  on public.product_events (visitor_id, created_at desc)
  where visitor_id is not null;

create index product_events_name_created_idx
  on public.product_events (event_name, created_at desc);

create index product_events_experiment_idx
  on public.product_events (experiment_id, created_at)
  where experiment_id is not null;

alter table public.product_events enable row level security;

revoke all on table public.product_events from public, anon, authenticated;
revoke all on sequence public.product_events_id_seq from public, anon, authenticated;
grant select, insert on table public.product_events to service_role;
grant usage, select on sequence public.product_events_id_seq to service_role;

insert into public.product_events (
  user_id, experiment_id, event_name, source, step, event_key, created_at
)
select
  ranked.user_id,
  ranked.id,
  'activation_completed',
  'onboarding',
  6,
  'activation:completed:' || ranked.id::text,
  ranked.activated_at
from (
  select
    experiments.*,
    row_number() over (partition by user_id order by activated_at, created_at) as activation_number
  from public.weekly_experiments experiments
  where activated_at is not null
) ranked
where ranked.activation_number = 1
on conflict (event_key) do nothing;

insert into public.product_events (
  user_id, experiment_id, event_name, source, event_key, created_at
)
select
  completions.user_id,
  completions.experiment_id,
  'practice_completed',
  'today',
  'practice:' || completions.experiment_id::text || ':' || completions.completion_date::text,
  completions.created_at
from public.daily_practice_completions completions
on conflict (event_key) do nothing;

insert into public.product_events (
