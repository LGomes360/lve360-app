create view analytics.funnel_weekly
with (security_invoker = true)
as
select
  date_trunc('week', created_at)::date as week_start,
  event_name,
  count(*)::bigint as events,
  count(distinct visitor_id)::bigint as unique_visitors,
  count(distinct user_id)::bigint as unique_members
from public.product_events
group by 1, 2;

create view analytics.weekly_activated_members
with (security_invoker = true)
as
select
  date_trunc('week', created_at)::date as week_start,
  count(distinct user_id)::bigint as weekly_activated_members
from public.product_events
where user_id is not null
  and event_name in ('practice_completed', 'weekly_review_completed')
group by 1;

create view analytics.retention_cohorts
with (security_invoker = true)
as
with first_activation as (
  select user_id, min(date_trunc('week', created_at)::date) as cohort_week
  from public.product_events
  where user_id is not null and event_name = 'activation_completed'
  group by user_id
),
cohort_sizes as (
  select cohort_week, count(*)::bigint as cohort_size
  from first_activation
  group by cohort_week
),
target_weeks as (
  select unnest(array[2, 4, 8, 12])::integer as week_number
),
member_activity as (
  select distinct
    user_id,
    date_trunc('week', created_at)::date as activity_week
  from public.product_events
  where user_id is not null
    and event_name in ('practice_completed', 'weekly_review_completed')
)
select
  sizes.cohort_week,
  targets.week_number,
  sizes.cohort_size,
  count(distinct activity.user_id)::bigint as active_members,
  round(count(distinct activity.user_id)::numeric / nullif(sizes.cohort_size, 0), 4) as retention_rate
from cohort_sizes sizes
cross join target_weeks targets
left join first_activation members on members.cohort_week = sizes.cohort_week
left join member_activity activity
  on activity.user_id = members.user_id
 and activity.activity_week = sizes.cohort_week + ((targets.week_number - 1) * 7)
group by sizes.cohort_week, targets.week_number, sizes.cohort_size;

revoke all on analytics.funnel_weekly from public, anon, authenticated;
revoke all on analytics.weekly_activated_members from public, anon, authenticated;
revoke all on analytics.retention_cohorts from public, anon, authenticated;
grant select on analytics.funnel_weekly to service_role;
grant select on analytics.weekly_activated_members to service_role;
grant select on analytics.retention_cohorts to service_role;

